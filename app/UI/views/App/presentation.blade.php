            <section id="work" class="h-full pb-3">
                <p>Esta plataforma digital faz parte do&nbsp;<strong>Projeto Allan Kardec</strong>, que tem por
                    principal objetivo permitir o acesso do público em geral e de pesquisadores a centenas de
                    manuscritos e documentos originais de Allan Kardec, a maioria dos quais nunca haviam sido divulgados
                    e editados. Assim, o projeto pretende se tornar referência para fontes históricas primárias ligadas
                    ao pensador francês Allan Kardec, nos moldes de iniciativas semelhantes, que disponibilizam os
                    manuscritos de autores influentes no mundo intelectual, tais como:</p>


                <ul>
                    <li><strong><a href="http://www.newtonproject.ox.ac.uk/" target="_blank" rel="noreferrer noopener">The
                                Newton Project</a></strong>, da Universidade de Oxford.
                    </li>
                    <li><strong><a href="https://www.darwinproject.ac.uk/" target="_blank" rel="noreferrer noopener">Darwin
                                Correspondence Project</a></strong>, da Universidade de Cambridge.
                    </li>
                </ul>
                <p>Arquivos e plataformas digitais para disponibilização de textos-chave de tradições religiosas
                    mantidos por diversas universidades internacionais de destaque também serviram de modelo. Por
                    exemplo:</p>
                <ul>
                    <li><strong><a href="https://www.codexsinaiticus.org/en/">Codex Sinaiticus Project</a></strong>, é
                        um dos mais antigos manuscritos da Bíblia Cristã em grego. Parceria entre a Leipzig University e
                        British Library, St Catherine’s Monastery e The National Library of Russia.
                    </li>
                    <li><strong><a href="https://cudl.lib.cam.ac.uk/">Cambridge Digital Library</a></strong>, da
                        Universidade de Cambridge, é uma das maiores bibliotecasdigitais do mundo. Possui em seu acervo
                        inúmeros manuscritos de textos religiosos tais comocoleções de Christian Works,
                        IslamicManuscripts, HebrewManuscripts e SanskritManuscripts.
                    </li>
                </ul>
                <p>A implementação da plataforma digital “Projeto Allan Kardec” se alinha com as iniciativas
                    supracitadas tornando-se a principal fonte primária para estudos sobre Allan Kardec em todo o mundo,
                    potencializando pesquisas sobre o tema em diversas áreas do conhecimento.</p>
                <p></p>
