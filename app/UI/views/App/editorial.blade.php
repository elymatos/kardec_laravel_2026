            <section id="work" class="h-full pb-3">
                <p>Das duas principais coleções de manuscritos e documentos, a Coleção Canuto Abreu e a Coleção AKOL, a
                    única que foi alvo de prévio trabalho editorial foi a que pertencia a Canuto Abreu. Este, durante
                    anos, anotou, transcreveu e traduziu uma quantidade razoável de manuscritos, apesar de nunca os ter
                    publicado na íntegra. Vários desses manuscritos, dessas transcrições e traduções chegaram-nos em
                    condições precárias de conservação e sem nenhuma organização. Assim, uma primeira abordagem, ainda
                    em curso, objetivou higienizá-los e organizá-los, estabelecer certa indexação categorial e
                    cronológica. Concomitantemente, esses textos também passam por um necessário processo de revisão,
                    sobretudo para corrigir, completar e atualizar as transcrições e traduções. O restante dos
                    manuscritos da Coleção Canuto Abreu e a quase totalidade dos da Coleção AKOL ainda devem ser todos
                    organizados, transcritos e traduzidos. Todavia todos deverão estar sob as mesmas regras
                    editoriais.</p>


                <p>O principal critério que adotamos nesta edição é o da maior fidelidade possível às idiossincrasias do
                    texto original, sem descuidar, porém, de estabelecer um equilíbrio entre essa fidelidade e a fluidez
                    da leitura do texto editado.</p>


                <p>Em parte, devido ao estilo caligráfico próprio do século XIX, em parte, por não se constituir
                    sobremaneira de versões definitivas, ou mesmo originalmente destinadas à publicação, mas de
                    rascunhos e psicografias, é compreensível que vários desses manuscritos apresentem lacunas,
                    repetições e hesitações gramaticais, tornando-se assim de difícil leitura direta. Desse modo,
                    propusemos a realização de dois tipos de transcrições: uma, simplificada, de todos os manuscritos, a
                    ser disponibilizada numa primeira etapa (em formato html e pdf); a outra, diplomática, de
                    determinados manuscritos, a ser disponibilizada numa segunda etapa, e que contenha obviamente a
                    representação de palavras e passagens rasuradas e outros elementos gráficos.</p>


                <p>Atentamos que as transcrições, simplificadas e diplomáticas, e traduções aqui a serem divulgadas não
                    são necessariamente versões definitivas. Oportunamente, muitas delas serão atualizadas (como é
                    possível verificar pelas datas de postagem). O intento é sempre corrigi-las e aprimorá-las, por
                    exemplo, quando ocorrer a descoberta da leitura correta de uma palavra ou passagem antes ilegível ou
                    incerta. Aparato crítico mínimo sobre o conteúdo desses textos também será posteriormente
                    acrescentado.</p>


                <p>Listamos a seguir as principais notações utilizadas nas transcrições e traduções:&nbsp;&nbsp;</p>


                <p>– Datas e outras informações conjecturadas são representadas entre colchetes: [1857].</p>


                <p>– Palavra e passagem com caligrafia distinta da principal são representadas entre colchetes
                    invertidos: ]<em>vocabulaire</em>[ = ]vocabulário[; e, para cada nova caligrafia, acrescentamos mais
                    um colchete invertido: ]]<em>vocabulaire</em>[[ = ]]vocabulário[[.</p>


                <p>– Palavra e passagem sublinhada no manuscrito são representadas com&nbsp;sublinhado&nbsp;ou com&nbsp;<em>itálico</em>.
                </p>


                <p>– Palavra e passagem&nbsp;<em>illisible</em>&nbsp;ou ilegível são representadas entre colchetes:
                    [<em>illis</em>.] = [ileg.].</p>


                <p>– Palavra e passagem de leitura incerta são representadas entre&nbsp;<em>chevrons</em>: &lt;<em>vocabulaire</em>&gt;
                    = &lt;vocabulário&gt;.&nbsp;&nbsp;&nbsp;&nbsp;</p>


                <p>– Palavra e passagem ausentes são representadas entre colchetes: […].</p>


                <p>– Palavra e passagem não localizadas no corpo central do manuscrito são representadas entre chaves na
                    transcrição:&nbsp;<em>vocabulaire</em>&nbsp;{<em>vocabulaire</em>}.</p>


                <p>Este trabalho não tem fins lucrativos; seu desenvolvimento seguirá o ritmo das pesquisas, das
                    transcrições e traduções, realizadas, em sua maioria, por voluntários. A eles, nossos sinceros
                    agradecimentos, assim como ao Centro de Documentação e Obras Raras (CDOR), que mantém, sobretudo
                    através do diligente trabalho de Eliana Almeida e de Flávio de Carvalho, a integridade do acervo de
                    Canuto Abreu; ao Museu Virtual AllanKardec.online (AKOL), que vigorosamente conseguiu resgatar outra
                    parte dos manuscritos e documentos de Kardec; à Universidade Federal de Juiz de Fora (UFJF), que
                    generosamente aceitou desenvolver este portal; a todos que, com sugestões e críticas, também vêm
                    contribuindo com este trabalho.&nbsp;&nbsp;</p>
