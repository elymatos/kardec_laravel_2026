            <section id="work" class="h-full pb-3">
                <p>Este trabalho deverá ser o resultado de contínua pesquisa e esforço colaborativo: tem por objetivo
                    primeiro organizar, editar e publicar a maior quantidade possível de manuscritos e documentos de
                    Allan Kardec, de seus colaboradores e contemporâneos.</p>


                <p>Apesar de terem pertencido a um mesmo corpo textual&nbsp;–&nbsp;desenvolvido pelo próprio Kardec
                    durante seus anos de formação e, sobretudo, durante os anos nos quais atuou como pesquisador de
                    experiências espirituais e teórico do Espiritismo&nbsp;–, os manuscritos e documentos com os quais
                    estamos lidando atualmente pertencem a dois principais acervos privados.</p>


                <p>O primeiro acervo foi adquirido pelo pesquisador Silvino Canuto Abreu, em parte quando de suas
                    viagens para a França em meados do século XX. Atualmente pertence ao Instituto Canuto Abreu e está
                    sob a guarda e o compromisso de divulgação da Fundação Espírita André Luiz (FEAL), especificamente
                    do Centro de Documentação e Obras Raras (CDOR), localizado na cidade de São Paulo. Compreende uma
                    biblioteca, contendo obras clássicas sobre o Cristianismo, o Magnetismo Animal, o Sonambulismo e o
                    Espiritismo, manuscritos e documentos de Canuto Abreu e manuscritos e documentos de Kardec. Estes,
                    denominados de Coleção Canuto Abreu, constam principalmente de rascunhos de cartas e de cartas, de
                    breves anotações e reflexões, de preparações de preces e de evocações, algumas destas provavelmente
                    utilizadas nas constantes sessões mediúnicas da Sociedade Parisiense de Estudos Espíritas ou em
                    sessões mediúnicas particulares.</p>
                <p>O segundo acervo foi recentemente adquirido na Librairie Leymarie, estabelecimento historicamente
                    ligado ao Espiritismo e ao Ocultismo do século XIX, localizado na cidade de Paris. Atualmente
                    pertence à instituição virtual Museu AllanKardec.online (AKOL). Compreende obras clássicas sobre o
                    Espiritismo e manuscritos e documentos de Kardec. Estes, denominados de Coleção AKOL, constam
                    principalmente de cartas, de breves anotações e reflexões, de bilhetes, de supostos diálogos e
                    mensagens com espíritos, de psicografias, algumas destas recebidas na Sociedade Parisiense ou a ela
                    enviadas de diversos outros centros espíritas da Europa.</p>
                <p>Demais coleções, demais manuscritos e documentos avulsos poderão ser eventualmente pesquisados e
                    acrescentados a este trabalho.</p>
                <p>A maioria dos manuscritos de Kardec, tanto os de caráter privado como os de caráter público, tanto os
                    rascunhos como os originais das suas obras editadas em vida, aparentemente se perdeu. Nessas duas
                    coleções, o que resta, portanto, é um corpo textual extremamente fragmentado. Esperamos que, mesmo
                    nessas condições, esses manuscritos possam contribuir para a pesquisa filológica, histórica e
                    filosófica sobre o Espiritismo e movimentos semelhantes.</p>
                <p>Datados sobretudo do século XIX, e em idioma francês, esses manuscritos serão aqui disponibilizados
                    em formato digital, com as respectivas transcrições e traduções para o idioma português;
                    oportunamente, traduções em outros idiomas também poderão ser realizadas.</p>
            </section>
